// 3. To learn use of single dimensional array by defining the array dynamically

public class $3_DynamicArray {
    public static void main(String[] args) {
        int array[];
        array = new int[5];
        for (int i = 0; i < 5; i++) {
            array[i] = i;
            System.out.println(array[i]);
        }
    }

}
